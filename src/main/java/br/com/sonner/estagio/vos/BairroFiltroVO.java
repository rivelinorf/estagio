package br.com.sonner.estagio.vos;

public class BairroFiltroVO {
	private Long id;
	private String nome;
	private Long cidade;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Long getCidade() {
		return cidade;
	}
	public void setCidade(Long cidade) {
		this.cidade = cidade;
	}

}
